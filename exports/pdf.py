# ── Internal helpers ──────────────────────────────────────────────────────────

def _pdf_footer(fig, generated_at):
    fig.text(0.06, 0.035, 'Generated by Inventory Analytics System', fontsize=8.5, color='#64748B')
    fig.text(0.94, 0.035, generated_at, fontsize=8.5, color='#64748B', ha='right')


def _result_metrics(results):
    if not results:
        return {'K': '-', 'Silhouette': np.nan, 'DBI': np.nan}
    best = results[best_k_index(results)]
    return {'K': best['K'], 'Silhouette': best['Silhouette'], 'DBI': best['DBI']}


def _pdf_page(pdf, title, lines, table_df=None):
    generated_at = datetime.now().strftime('%d %B %Y %H:%M')
    fig = plt.figure(figsize=(11.69, 8.27))
    ax = fig.add_axes([0, 0, 1, 1])
    ax.axis('off')
    ax.text(0.06, 0.94, title, fontsize=18, weight='bold', color=PALETTE['bg_dark'])

    y = 0.88
    for line in lines:
        for chunk in wrap(str(line), 105):
            ax.text(0.06, y, chunk, fontsize=10.5, color=PALETTE['text_dark'])
            y -= 0.034
        y -= 0.006

    if table_df is not None and not table_df.empty:
        table_ax = fig.add_axes([0.05, 0.08, 0.90, max(0.22, y - 0.10)])
        table_ax.axis('off')
        show_df = table_df.head(16).copy()
        cell_text = show_df.astype(str).values.tolist()
        col_width = 1 / max(len(show_df.columns), 1)
        table = table_ax.table(
            cellText=cell_text,
            colLabels=show_df.columns.tolist(),
            cellLoc='left', colLoc='left',
            colWidths=[col_width] * len(show_df.columns),
            loc='upper left',
        )
        table.auto_set_font_size(False)
        table.set_fontsize(8)
        table.scale(1, 1.35)
        for (row, col), cell in table.get_celld().items():
            cell.set_edgecolor('#CBD5E1')
            if row == 0:
                cell.set_facecolor('#E8F1FB')
                cell.set_text_props(weight='bold', color=PALETTE['bg_dark'])
            else:
                cell.set_facecolor('white')
                cell.set_text_props(color=PALETTE['text_dark'])

    _pdf_footer(fig, generated_at)
    pdf.savefig(fig, bbox_inches='tight')
    plt.close(fig)


def _pdf_bar_page(pdf, title, series, xlabel):
    generated_at = datetime.now().strftime('%d %B %Y %H:%M')
    series = series.dropna().sort_values(ascending=True).tail(12)
    if series.empty:
        return

    fig, ax = plt.subplots(figsize=(11.69, 8.27))
    fig.patch.set_facecolor('white')
    ax.barh(series.index.astype(str), series.values, color=PALETTE['secondary'], edgecolor=PALETTE['bg_dark'])
    ax.set_title(title, fontsize=17, fontweight='bold', color=PALETTE['bg_dark'], pad=18)
    ax.set_xlabel(xlabel, fontsize=10, color=PALETTE['text_dark'])
    ax.tick_params(axis='both', labelsize=9, colors=PALETTE['text_dark'])
    ax.grid(axis='x', color='#E2E8F0', linewidth=0.8)
    ax.set_axisbelow(True)
    for spine in ax.spines.values():
        spine.set_color('#CBD5E1')
    for i, value in enumerate(series.values):
        ax.text(value, i, f' {value:,.0f}', va='center', fontsize=8.5, color=PALETTE['text_dark'])
    _pdf_footer(fig, generated_at)
    fig.tight_layout(rect=[0.03, 0.06, 0.98, 0.96])
    pdf.savefig(fig, bbox_inches='tight')
    plt.close(fig)


